    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Venda venda = new Venda();
        

        // Obtém os valores dos campos de texto
        String cliente = jTextCliente.getText();
        String cor = jTextCor.getText();
        String modelo = jTextModelo.getText();
        String veiculo = jTextVeiculo.getText();
        String anoFabricoText = jformAno.getText();
        String precoText = jformPreco.getText();

        // Verifica se algum campo obrigatório está vazio
        if (cliente.isEmpty() || cor.isEmpty() || modelo.isEmpty() || veiculo.isEmpty() || anoFabricoText.isEmpty() || precoText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de salvar.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

    try {
            // Define os valores na instância de Venda
            venda.setCliente(cliente);
            venda.setCor(cor);
            venda.setModelo(modelo);
            venda.setVeiculo(veiculo);
            venda.setAnoFabrico(Double.parseDouble(anoFabricoText));
            venda.setPreco(Double.parseDouble(precoText));
        } catch (NumberFormatException e) {
            // Exibe uma mensagem de erro se o usuário não inserir um número válido
            JOptionPane.showMessageDialog(this, "Erro ao converter o ano de fabricação ou o preço para Double.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
        // Tenta inserir a venda no banco de dados
        try (Connection conexao = ConexaoBD.conectar()) {
            VendaDAO vendaDAO = new VendaDAO(conexao);
            vendaDAO.inserir(venda);

            // Limpa os campos de texto
            limparCampos();

            // Verifica se 'menu' foi inicializado antes de chamar o método
            if (menu != null) {
                // Atualiza os dados na tabela tbGrade1
                menu.visualizarDados();
            } else {
                System.err.println("A instância 'Menu' foi inicializada.");
            }

            // Exibe uma mensagem de sucesso
            JOptionPane.showMessageDialog(this, "Dados guardados com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            // Exibe uma mensagem de erro se houver um problema ao conectar ou inserir no banco de dados
            JOptionPane.showMessageDialog(this, "Erro ao guardar dados no banco de dados.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception ex) {
        // Adicione um bloco catch mais amplo para lidar com exceções
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnSalvarActionPerformed

