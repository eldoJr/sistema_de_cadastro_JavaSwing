    private void visualizarDados() {
        try (Connection conexao = ConexaoBD.conectar()) {
            // Cria uma nova instância de VendaDAO
            VendaDAO vendaDAO = new VendaDAO(conexao);

            // Recupera todas as vendas do banco de dados
            List<Venda> vendas = vendaDAO.listarTodos();

            // Cria uma nova instância de VendaTableModel e define os dados
            VendaTableModel vendaTableModel = new VendaTableModel();
            vendaTableModel.setDados(vendas);

            // Define o modelo da sua tabela para o VendaTableModel
        } catch (SQLException e) {
            // Trata o erro se houver um problema ao conectar ou listar do banco de dados
            System.out.println("Erro ao visualizar dados do banco de dados.");
            e.printStackTrace();
        }
    }
